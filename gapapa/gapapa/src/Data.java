
public class Data {
    private String nama;
    private String no_telp;

    public Data(String nama, String no_telp){
        this.nama = nama;
        this.no_telp = no_telp;
    }

    public String getNama() {
        return nama;
    }
    public String getNo_telp() {
        return no_telp;
    }

}
